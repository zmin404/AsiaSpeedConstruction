            'type' => ''
        ), $atts ) );
        wp_enqueue_script('tab');
        global $tabs_counter, $indiv_tab_counter, $tabs_single;
        $tabs_counter++;
        $sub_tabs_counter = 1;
        $indiv_tab_counter = 0;
        $tabs_single = 0;
        /* Everything inside [tab] shortcode */
        preg_match_all( '#\[tab(.*?)\]#', $content, $matches);
        if ( isset($matches[1]) ) { $tab_titles = $matches[1]; }
        $class = "";
        $class_before = "";
